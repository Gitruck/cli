/** IR fx op → AE 原生特效 matchName（已验证的常识映射；无把握的一律进 TODO 注释） */
const FX_MATCHNAME: Record<string, string> = {
  glow: "ADBE Glo2", // Glow
  blur: "ADBE Gaussian Blur 2", // Gaussian Blur
  dirBlur: "ADBE Motion Blur", // Directional Blur
  turbulence: "ADBE Turbulent Displace",
  tile: "ADBE Tile", // Motion Tile
  dropShadow: "ADBE Drop Shadow",
  invert: "ADBE Invert",
  wipe: "ADBE Linear Wipe",
  bulge: "ADBE Bulge",
  noise: "ADBE Noise",
  fill: "ADBE Fill",
  vignette: "ADBE Vignette", // Lumetri 之外的独立 Vignette（较新 AE），失败会被 try/catch 吞掉
  colorAdjust: "ADBE Brightness & Contrast 2",
};

function esc(s: string): string {
  return String(s)
    .replace(/\\/g, "\\\\")
    .replace(/"/g, '\\"')
    .replace(/\r/g, "\\r")
    .replace(/\n/g, "\\n")
    .replace(/\t/g, "\\t")
    // 行分隔符会截断 ExtendScript 字符串字面量（U+2028/U+2029，用 fromCharCode 避免源码里出现裸分隔符）
    .replace(new RegExp(String.fromCharCode(0x2028), "g"), "\\u2028")
    .replace(new RegExp(String.fromCharCode(0x2029), "g"), "\\u2029");
}

function r3(v: number): number {
  return Math.round(v * 1000) / 1000;
}

function colorArr(css: string | undefined, fallback: [number, number, number]): [number, number, number] {
  const c = parseCssColor(css);
  if (!c) return fallback;
  return [r3(c[0] / 255), r3(c[1] / 255), r3(c[2] / 255)];
}

/**
 * cubic-bezier → AE temporal ease 尽力映射：
 * 出向 influence ≈ x1*100，入向 influence ≈ (1-x2)*100（clamp 到 AE 允许的 [0.1,100]），speed=0。
 * 这是常见的"平滑进出"近似——精确速度曲线不可逆推，保形不保速。
 */
function easeInfluence(e: string | null | undefined): { out: number; inn: number } | null {
  const cb = parseCubicBezier(e);
  if (!cb) return null;
  const clamp = (v: number) => Math.min(100, Math.max(0.1, v));
  return { out: clamp(cb[0] * 100 || 33), inn: clamp((1 - cb[2]) * 100 || 33) };
}

/** MAD 素材注入映射：layerId → footage 路径 + 取段偏移（秒）。 */
export interface FootageMap {
  [layerId: string]: { path: string; srcOffset?: number };
}

export interface JsxOpts {
  /** 产物 header 文案（默认「仅供学习研究」口径不变）。 */
  header?: string;
  /** 素材注入：layerId → footage（一窗口一素材由调用侧映射保证：同素材多 slot 用同 path 不同 srcOffset）。 */
  footage?: FootageMap;
  /** 时序算子烘焙开关（默认 false：保持现 TODO 注释路径，零回归）。 */
  bakeOps?: boolean;
  /** 内部：发射进指定合成变量（madJsx 子合成用），缺省 "comp"。 */
  compVar?: string;
}

interface Ctx {
  lines: string[];
  warnings: string[];
  layerSeq: number;
  compVar: string;
  footage: FootageMap;
  bakeOps: boolean;
  /** footage 路径 → 已 import 的 JSX 变量名（每路径仅 importFile 一次、复用）。 */
  footageVars: Map<string, string>;
}

/** 生成一条 setValueAtTime 序列 + ease */
function emitKeys(
  ctx: Ctx,
  propExpr: string,
  keys: { t: number; value: string; e?: string | null }[],
): void {
  if (keys.length === 0) return;
  const L = ctx.lines;
  L.push(`  try {`);
  L.push(`    var p = ${propExpr};`);
  for (const k of keys) {
    L.push(`    p.setValueAtTime(${r3(k.t)}, ${k.value});`);
  }
  // temporal ease（逐 key 尽力）
  keys.forEach((k, i) => {
    const inf = easeInfluence(k.e);
    if (!inf) return;
    L.push(`    try {`);
    L.push(`      var dim = 1; try { dim = p.value.length || 1; } catch (e) { dim = 1; }`);
    L.push(`      var eo = [], ei = [];`);
    L.push(
      `      for (var d = 0; d < Math.min(dim, 3); d++) { eo.push(new KeyframeEase(0, ${r3(inf.out)})); ei.push(new KeyframeEase(0, ${r3(inf.inn)})); }`,
    );
    L.push(`      p.setTemporalEaseAtKey(${i + 1}, ei, eo);`);
    L.push(`    } catch (e) {}`);
  });
  L.push(`  } catch (e) {}`);
}

function fxComment(fx: IRFx): string {
  const params = Object.keys(fx)
    .filter((k) => !["op", "src", "tracks"].includes(k))
    .map((k) => `${k}=${JSON.stringify((fx as Record<string, unknown>)[k])}`)
    .join(" ");
  const tracks = fx.tracks ? ` tracks:[${Object.keys(fx.tracks).join(",")}]` : "";
  return `${fx.op}${fx.src ? ` (原特效: ${fx.src})` : ""}${params ? " " + params : ""}${tracks}`;
}

function emitEffects(ctx: Ctx, layerVar: string, fxList: IRFx[] | undefined): void {
  if (!fxList || fxList.length === 0) return;
  const L = ctx.lines;
  L.push(`  // —— 特效（原工程特效清单，已知 matchName 自动 applyEffect，其余 TODO 手动补）——`);
  for (const fx of fxList) {
    const mn = FX_MATCHNAME[fx.op];
    // 烘焙开时时序算子由 emitBakedOps 承接（不再输出 TODO、不 addProperty），跳过
    if (ctx.bakeOps && TIME_DOMAIN_OPS.has(fx.op)) continue;
    if (mn && !TIME_DOMAIN_OPS.has(fx.op)) {
      L.push(`  // fx: ${esc(fxComment(fx))}`);
      L.push(`  try { ${layerVar}.property("ADBE Effect Parade").addProperty("${mn}"); } catch (e) {}`);
    } else if (TIME_DOMAIN_OPS.has(fx.op)) {
      L.push(`  // TODO fx(时序算子,请用表达式或关键帧手动复现): ${esc(fxComment(fx))}`);
      ctx.warnings.push(`jsx: 时序算子 ${fx.op} 未自动铺帧（已写 TODO 注释）`);
    } else {
      L.push(`  // TODO fx(无对应 AE 原生 matchName): ${esc(fxComment(fx))}`);
      ctx.warnings.push(`jsx: 未映射特效 ${fx.op}（已写 TODO 注释）`);
    }
  }
}

// ---------------------------------------------------------------- 烘焙轨合并（bakeOps 时）

/** 一条待发射的绝对关键帧：value 已格式化为 JSX 字面量。 */
interface AbsKey {
  t: number;
  value: string;
  e?: string | null;
}

/** 基轨采样：位置/缩放倍数（供窗界锚点用）。 */
function samplePos(anim: IRAnim, pos: [number, number], t: number): [number, number] {
  return [sampleTrack(anim.x, t, pos[0]), sampleTrack(anim.y, t, pos[1])];
}
function sampleScaleMul(anim: IRAnim, t: number): [number, number] {
  if (anim.scale?.length) {
    const s = sampleTrack(anim.scale, t, 1);
    return [s, s];
  }
  if (anim.sx?.length || anim.sy?.length) return [sampleTrack(anim.sx, t, 1), sampleTrack(anim.sy, t, 1)];
  return [1, 1];
}

/**
 * 合并基轨关键帧与烘焙轨：窗内基轨帧抑制、baked 帧铺入、窗界补基轨锚点。
 * baseKeys 为已格式化的基轨帧（含 ease）；bakedKeys 为 baked 轨帧（线性，无 ease）。
 * windows 为各烘焙窗；layerIn/Out 用于锚点边界钳制。
 */
function mergeBaked(
  baseKeys: AbsKey[],
  bakedKeys: AbsKey[],
  windows: [number, number][],
  anchorAt: (t: number) => string,
  layerIn: number,
  layerOut: number,
): AbsKey[] {
  const inWindow = (t: number): boolean => {
    for (const w of windows) if (t > w[0] + 1e-6 && t < w[1] - 1e-6) return true;
    return false;
  };
  const out: AbsKey[] = [];
  // 窗外基轨帧保留（含 ease）
  for (const k of baseKeys) if (!inWindow(k.t)) out.push(k);
  // 窗界基轨锚点（退出后回落基轨，避免停在算子末值）
  const EPS = 0.033; // ~1 帧@30fps
  for (const w of windows) {
    if (w[1] < layerOut - EPS) {
      const at = Math.min(w[1] + EPS, layerOut);
      out.push({ t: at, value: anchorAt(at), e: null });
    }
    if (w[0] > layerIn + EPS) {
      const at = Math.max(w[0] - EPS, layerIn);
      out.push({ t: at, value: anchorAt(at), e: null });
    }
  }
  // baked 帧
  out.push(...bakedKeys);
  // 排序 + 去重（同刻取后者，优先 baked）
  out.sort((a, b) => a.t - b.t);
  const ded: AbsKey[] = [];
  for (const k of out) {
    if (ded.length && Math.abs(k.t - ded[ded.length - 1].t) < 1e-6) ded[ded.length - 1] = k;
    else ded.push(k);
  }
  return ded;
}

/** 基轨 position 帧（x/y 合并、绝对 px）。 */
function positionBaseKeys(anim: IRAnim, pos: [number, number]): AbsKey[] {
  if (!(anim.x?.length || anim.y?.length)) return [];
  const merged = mergeChannelTracks(anim.x, anim.y, pos[0], pos[1]);
  return merged.map((k) => ({ t: k.t, value: `[${r3(k.a)}, ${r3(k.b)}]`, e: k.e }));
}

/** 基轨 scale 帧（→ 百分数；footage 乘 cover）。 */
function scaleBaseKeys(anim: IRAnim, coverExpr: string | null): AbsKey[] {
  const sc = (v: number): string => (coverExpr ? `${r3(v)}*${coverExpr}` : `${r3(v)}`);
  if (anim.scale?.length) {
    return anim.scale.map((k) => ({ t: k.t, value: `[${sc(num(k.v, 1) * 100)}, ${sc(num(k.v, 1) * 100)}]`, e: k.e }));
  }
  if (anim.sx?.length || anim.sy?.length) {
    const merged = mergeChannelTracks(anim.sx, anim.sy, 1, 1);
    return merged.map((k) => ({ t: k.t, value: `[${sc(k.a * 100)}, ${sc(k.b * 100)}]`, e: k.e }));
  }
  return [];
}

/** 烘焙轨发射（position/scale/rotation 合并进基轨；brightness→亮度对比度特效）。 */
function emitBakedOps(ctx: Ctx, ly: IRLayer, layerVar: string, tf: string, coverExpr: string | null): void {
  const { tracks, warnings } = bakeLayerOps(ly);
  for (const w of warnings) ctx.warnings.push(w);
  if (tracks.length === 0) return;
  const anim = ly.anim ?? {};
  const pos: [number, number] = [num(ly.pos?.[0], 0), num(ly.pos?.[1], 0)];
  const inn = num(ly.in, 0);
  const out = Math.max(inn + 0.01, num(ly.out, inn + 1));
  const sc = (v: number): string => (coverExpr ? `${r3(v)}*${coverExpr}` : `${r3(v)}`);

  // brightness（flicker）→ 亮度对比度特效参数轨
  const brights = tracks.filter((t) => t.prop === "brightness");
  if (brights.length) {
    ctx.lines.push(`  // fx: flicker → 亮度脉冲（ADBE Brightness & Contrast 2，避开 opacity 通道）`);
    ctx.lines.push(
      `  var flkFx = null; try { flkFx = ${layerVar}.property("ADBE Effect Parade").addProperty("ADBE Brightness & Contrast 2"); } catch (e) { flkFx = null; }`,
    );
    for (const bt of brights) {
      const keys = bt.times.map((t, i) => ({ t, value: `${r3(bt.values[i][0])}`, e: null }));
      emitKeys(ctx, `flkFx.property(1)`, keys);
    }
  }

  // position
  const posBaked = tracks.filter((t) => t.prop === "position");
  if (posBaked.length) {
    const baseKeys = positionBaseKeys(anim, pos);
    const bakedKeys: AbsKey[] = [];
    const windows: [number, number][] = [];
    for (const bt of posBaked) {
      windows.push(bt.window);
      for (let i = 0; i < bt.times.length; i++) {
        bakedKeys.push({ t: bt.times[i], value: `[${r3(bt.values[i][0])}, ${r3(bt.values[i][1])}]`, e: null });
      }
    }
    const merged = mergeBaked(
      baseKeys,
      bakedKeys,
      windows,
      (t) => {
        const [x, y] = samplePos(anim, pos, t);
        return `[${r3(x)}, ${r3(y)}]`;
      },
      inn,
      out,
    );
    emitKeys(ctx, `${tf}.property("ADBE Position")`, merged);
  }

  // scale（baked 值已为百分数；footage 乘 cover）
  const scaleBaked = tracks.filter((t) => t.prop === "scale");
  if (scaleBaked.length) {
    const baseKeys = scaleBaseKeys(anim, coverExpr);
    const bakedKeys: AbsKey[] = [];
    const windows: [number, number][] = [];
    for (const bt of scaleBaked) {
      windows.push(bt.window);
      for (let i = 0; i < bt.times.length; i++) {
        bakedKeys.push({ t: bt.times[i], value: `[${sc(bt.values[i][0])}, ${sc(bt.values[i][1])}]`, e: null });
      }
    }
    const merged = mergeBaked(
      baseKeys,
      bakedKeys,
      windows,
      (t) => {
        const [mx, my] = sampleScaleMul(anim, t);
        return `[${sc(mx * 100)}, ${sc(my * 100)}]`;
      },
      inn,
      out,
    );
    emitKeys(ctx, `${tf}.property("ADBE Scale")`, merged);
  }

  // rotation
  const rotBaked = tracks.filter((t) => t.prop === "rotation");
  if (rotBaked.length) {
    const baseKeys = (anim.rot ?? []).map((k) => ({ t: k.t, value: `${r3(num(k.v, 0))}`, e: k.e }));
    const bakedKeys: AbsKey[] = [];
    const windows: [number, number][] = [];
    for (const bt of rotBaked) {
      windows.push(bt.window);
      for (let i = 0; i < bt.times.length; i++) bakedKeys.push({ t: bt.times[i], value: `${r3(bt.values[i][0])}`, e: null });
    }
    const merged = mergeBaked(baseKeys, bakedKeys, windows, (t) => `${r3(sampleTrack(anim.rot, t, 0))}`, inn, out);
    emitKeys(ctx, `${tf}.property("ADBE Rotate Z")`, merged);
  }
}

/** 单层发射。parentNullVar: 所属 group 的 null 层变量名（无则 null） */
function emitLayer(ctx: Ctx, ir: IRProject, ly: IRLayer, parentNullVar: string | null): void {
  const L = ctx.lines;
  const cv = ctx.compVar;
  const c = ir.canvas;
  const id = ++ctx.layerSeq;
  const v = `ly${id}`;
  const inn = num(ly.in, 0);
  const out = Math.max(inn + 0.01, num(ly.out, c.duration));
  const px = num(ly.pos?.[0], c.w / 2);
  const py = num(ly.pos?.[1], c.h / 2);
  const name = esc(`${ly.id || "L" + id} [${ly.type}]`);

  L.push(``);
  L.push(`  // ---- 层 ${name} in=${r3(inn)} out=${r3(out)} ----`);

  if (ly.type === "group") {
    // 组 → null 层，子层挂其下（组通常是透明容器：null 摆在 [0,0]，子层用画布绝对坐标，
    // 组动画通过 null 关键帧整体带动；组自身 rot/scale 动画场景极罕见，不做）
    L.push(`  var ${v} = ${cv}.layers.addNull(${r3(c.duration)});`);
    L.push(`  ${v}.name = "${name}";`);
    L.push(`  ${v}.inPoint = ${r3(inn)}; ${v}.outPoint = ${r3(out)};`);
    L.push(`  ${v}.property("ADBE Transform Group").property("ADBE Anchor Point").setValue([0,0]);`);
    L.push(`  ${v}.property("ADBE Transform Group").property("ADBE Position").setValue([0,0]);`);
    if (parentNullVar) L.push(`  try { ${v}.parent = ${parentNullVar}; } catch (e) {}`);
    emitGroupAnim(ctx, v, ly);
    emitEffects(ctx, v, ly.fx);
    for (const child of ly.children ?? []) emitLayer(ctx, ir, child, v);
    return;
  }

  // MAD 素材注入：image/video 且 footage 映射命中 → footage 层（cover 缩放 + srcOffset 取段）
  let coverExpr: string | null = null;
  const footageHit = ly.type === "image" || ly.type === "video" ? ctx.footage[ly.id] : undefined;
  const fgVar = footageHit ? ctx.footageVars.get(footageHit.path) : undefined;

  if (footageHit && fgVar) {
    const slotW = Math.max(2, Math.round(num(ly.w, c.w)));
    const slotH = Math.max(2, Math.round(num(ly.h, c.h)));
    const srcOffset = num(footageHit.srcOffset, 0);
    const cvv = `cv${id}`;
    coverExpr = cvv;
    L.push(`  // 素材注入(${esc(String(ly.type))}): footage srcOffset=${r3(srcOffset)}`);
    L.push(`  var ${v} = null, ${cvv} = 1;`);
    L.push(`  if (${fgVar} != null) {`);
    L.push(`    try {`);
    L.push(`      ${v} = ${cv}.layers.add(${fgVar});`);
    L.push(`      var _fw = ${fgVar}.width || ${slotW}, _fh = ${fgVar}.height || ${slotH};`);
    L.push(`      ${cvv} = Math.max(${slotW} / _fw, ${slotH} / _fh);`);
    L.push(`      ${v}.startTime = ${r3(inn)} - ${r3(srcOffset)};`);
    L.push(`    } catch (e) { ${v} = null; }`);
    L.push(`  }`);
    L.push(`  if (${v} == null) {`);
    L.push(`    ${v} = ${cv}.layers.addSolid([0.35,0.35,0.4], "${name} [占位]", ${slotW}, ${slotH}, 1, ${r3(c.duration)});`);
    L.push(`    ${cvv} = 1;`);
    L.push(`  }`);
  } else if (ly.type === "text") {
    const size = fontPx(ly.font);
    const col = colorArr(ly.color, [1, 1, 1]);
    L.push(`  var ${v} = ${cv}.layers.addText("${esc(ly.text ?? "")}");`);
    L.push(`  try {`);
    L.push(`    var td = ${v}.property("ADBE Text Properties").property("ADBE Text Document");`);
    L.push(`    var tdv = td.value;`);
    L.push(`    tdv.fontSize = ${Math.round(size)};`);
    L.push(`    tdv.fillColor = [${col.join(",")}];`);
    L.push(`    tdv.justification = ParagraphJustification.CENTER_JUSTIFY;`);
    L.push(`    td.setValue(tdv);`);
    L.push(`  } catch (e) {}`);
  } else if (ly.type === "shape") {
    // 形状层：占位用固态（rect 语义等价；ellipse 记 TODO）
    const col = colorArr(ly.fill, [0.2, 0.33, 0.67]);
    const w = Math.max(2, Math.round(num(ly.w, 400)));
    const h = Math.max(2, Math.round(num(ly.h, 400)));
    if (ly.shape === "ellipse") L.push(`  // TODO: 原层为椭圆形状,固态占位,可手动换 shape layer`);
    L.push(`  var ${v} = ${cv}.layers.addSolid([${col.join(",")}], "${name}", ${w}, ${h}, 1, ${r3(c.duration)});`);
  } else {
    // image / video / null / 未知 → 灰色固态占位
    const w = Math.max(2, Math.round(num(ly.w, c.w)));
    const h = Math.max(2, Math.round(num(ly.h, c.h)));
    L.push(`  // 占位素材(${esc(String(ly.type))}${ly.asset ? ` asset=${esc(String(ly.asset))}` : ""}): 请替换为真实素材`);
    L.push(`  var ${v} = ${cv}.layers.addSolid([0.35,0.35,0.4], "${name} [占位]", ${w}, ${h}, 1, ${r3(c.duration)});`);
  }

  L.push(`  ${v}.name = "${name}";`);
  L.push(`  ${v}.inPoint = ${r3(inn)}; ${v}.outPoint = ${r3(out)};`);
  if (parentNullVar) L.push(`  try { ${v}.parent = ${parentNullVar}; } catch (e) {}`);
  if (ly.blend && ly.blend !== "normal") {
    L.push(`  // 混合模式: ${esc(ly.blend)}`);
    const bm = BLEND_JSX[ly.blend];
    if (bm) L.push(`  try { ${v}.blendingMode = BlendingMode.${bm}; } catch (e) {}`);
  }

  const tf = `${v}.property("ADBE Transform Group")`;
  L.push(`  ${tf}.property("ADBE Position").setValue([${r3(px)}, ${r3(py)}]);`);

  const anim = ly.anim ?? {};
  // 烘焙开且该层有时序算子命中的属性 → 由 emitBakedOps 合并发射，此处跳过对应基轨
  const bakedProps = new Set(ctx.bakeOps ? bakeLayerOps(ly).tracks.map((t) => t.prop) : []);

  // position（x/y 双轨合并，值=画布绝对 px）
  if ((anim.x?.length || anim.y?.length) && !bakedProps.has("position")) {
    emitKeys(ctx, `${tf}.property("ADBE Position")`, positionBaseKeys(anim, [px, py]));
  }
  // scale（scale 统一 / sx,sy 分轨；AE 百分数；footage 乘 cover）
  if (!bakedProps.has("scale")) {
    const scKeys = scaleBaseKeys(anim, coverExpr);
    if (scKeys.length) emitKeys(ctx, `${tf}.property("ADBE Scale")`, scKeys);
    else if (coverExpr) {
      // footage 无缩放动画 → 静态 cover 缩放充满 slot
      L.push(`  ${tf}.property("ADBE Scale").setValue([100*${coverExpr}, 100*${coverExpr}]);`);
    }
  }
  // rotation（度）
  if (anim.rot?.length && !bakedProps.has("rotation")) {
    emitKeys(
      ctx,
      `${tf}.property("ADBE Rotate Z")`,
      anim.rot.map((k) => ({ t: k.t, value: `${r3(num(k.v, 0))}`, e: k.e })),
    );
  }
  // opacity（IR [0,1] → AE %）
  if (anim.opacity?.length) {
    emitKeys(
      ctx,
      `${tf}.property("ADBE Opacity")`,
      anim.opacity.map((k) => ({ t: k.t, value: `${r3(Math.min(1, Math.max(0, num(k.v, 1))) * 100)}`, e: k.e })),
    );
  }
  if (anim.ls?.length) {
    L.push(`  // TODO: letterspacing 轨未自动映射（AE 需 Animator>Tracking），共 ${anim.ls.length} 帧`);
  }

  emitEffects(ctx, v, ly.fx);
  if (ctx.bakeOps) emitBakedOps(ctx, ly, v, tf, coverExpr);
  if (ly.raw_fx?.length) {
    L.push(`  // TODO 原工程还有未解析特效: ${esc(ly.raw_fx.join(", "))}`);
  }
}

/** 组动画：x/y 轨降到 null（偏移 = v - pos），其余通道注释说明 */
function emitGroupAnim(ctx: Ctx, v: string, ly: IRLayer): void {
  const anim = ly.anim ?? {};
  const px = num(ly.pos?.[0], 0);
  const py = num(ly.pos?.[1], 0);
  if (anim.x?.length || anim.y?.length) {
    const merged = mergeChannelTracks(anim.x, anim.y, px, py);
    emitKeys(
      ctx,
      `${v}.property("ADBE Transform Group").property("ADBE Position")`,
      merged.map((k) => ({ t: k.t, value: `[${r3(k.a - px)}, ${r3(k.b - py)}]`, e: k.e })),
    );
  }
  for (const ch of ["scale", "rot", "opacity"] as const) {
    if (anim[ch]?.length) {
      ctx.lines.push(`  // TODO: 组自身 ${ch} 动画未映射到 null（子层坐标为画布绝对值,直接缩放会偏移）`);
      ctx.warnings.push(`jsx: 组 ${ly.id} 的 ${ch} 动画需手动复现`);
    }
  }
}

