/**
 * IR v1 → After Effects ExtendScript (.jsx) 构建脚本。
 *
 * 生成的脚本在 AE 里跑一遍即重建合成：canvas 尺寸/时长/fps、逐层固态/文本/占位层、
 * pos/scale/rotation/opacity 关键帧 + temporal ease 尽力映射；fx 已知 matchName 的生成
 * applyEffect 调用，未知的写 TODO 注释。
 *
 * 反向参考：ae_export_kit/dump_aep.jsx（本文件即其"读"操作的"写"镜像）。
 *
 * MAD 扩展（add-tool-mad，全部 opt-in——不传 opts 时输出与迁入前逐字节一致，见 ir-convert-library spec）：
 *   - opts.header：产物 header 可配置（默认「仅供学习研究」不变）
 *   - opts.footage：素材注入（layerId → footage 路径 + srcOffset），一窗口一素材共用、缺文件回退灰固态
 *   - opts.bakeOps：时序算子烘焙（shake/pulsate/oscillate/zoom/flicker → 关键帧，见 bake_ops.ts）
 *   - madJsx()：母合成拼接（子合成完整铺 + 三属性裁窗 + BGM 入轨 + beat marker）
 */
