/** 路径正斜杠化（footage import 用；纯字符串处理，不做 IO）。 */
function fwdSlash(p: string): string {
  return p.replace(/\\/g, "/");
}

/** footage import 注册段：每个唯一路径 importFile 一次、缓存到变量。写进 ctx.footageVars。 */
function emitFootageImports(ctx: Ctx, paths: string[]): void {
  const L = ctx.lines;
  const uniq = [...new Set(paths)];
  if (uniq.length === 0) return;
  L.push(`  // —— 素材导入（每素材仅导入一次、变量缓存复用）——`);
  let i = 0;
  for (const p of uniq) {
    const vn = `fg${++i}`;
    ctx.footageVars.set(p, vn);
    L.push(`  var ${vn} = null;`);
    L.push(
      `  try { ${vn} = app.project.importFile(new ImportOptions(new File("${esc(fwdSlash(p))}"))); } catch (e) { ${vn} = null; }`,
    );
  }
}

/** 建一个 Ctx。 */
function newCtx(opts: JsxOpts | undefined): Ctx {
  return {
    lines: [],
    warnings: [],
    layerSeq: 0,
    compVar: opts?.compVar ?? "comp",
    footage: opts?.footage ?? {},
    bakeOps: !!opts?.bakeOps,
    footageVars: new Map(),
  };
}

/** IR → ExtendScript 文本 */
export function irToJsx(ir: IRProject, opts?: JsxOpts): { jsx: string; warnings: string[] } {
  const c = ir.canvas;
  const ctx = newCtx(opts);
  const compName = esc(ir.id || "ir-comp");
  const bg = colorArr(c.bg, [0.04, 0.04, 0.07]);
  const w = Math.max(4, Math.round(num(c.w, 1920)));
  const h = Math.max(4, Math.round(num(c.h, 1080)));
  const fps = Math.min(99, Math.max(1, num(c.fps, 30)));
  const dur = Math.max(0.1, num(c.duration, 3));

  const L = ctx.lines;
  L.push(opts?.header ?? HEADER);
  L.push(`// 源: ${esc(String(ir.source ?? ir.id ?? ""))}`);
  L.push(``);
  L.push(`app.beginUndoGroup("IR Rebuild ${compName}");`);
  L.push(`(function () {`);
  L.push(`  var comp = app.project.items.addComp("${compName}", ${w}, ${h}, 1, ${r3(dur)}, ${r3(fps)});`);
  L.push(`  try { comp.bgColor = [${bg.join(",")}]; } catch (e) {}`);

  // footage 导入（有素材注入映射时）
  if (opts?.footage && Object.keys(opts.footage).length) {
    emitFootageImports(
      ctx,
      Object.values(opts.footage).map((f) => f.path),
    );
  }

  // IR 绘制序：第一个在最底。AE addLayer 每次压到最上 → 顺序遍历即天然吻合（后加的在上）。
  for (const ly of ir.layers ?? []) emitLayer(ctx, ir, ly, null);

  L.push(``);
  L.push(`  comp.openInViewer();`);
  L.push(`})();`);
  L.push(`app.endUndoGroup();`);
  return { jsx: L.join("\n") + "\n", warnings: ctx.warnings };
}

export function exportJsx(ir: IRProject): ExportResult {
  const { jsx, warnings } = irToJsx(ir);
  return {
    filename: `${ir.id || "ir"}.jsx`,
    mime: "application/javascript",
    data: jsx,
    warnings,
  };
}

// ---------------------------------------------------------------- MAD 母合成拼接（D6）

/** 一个技法窗口：完整工程 IR + 裁窗 + 素材注入 + 母时间线落点。 */
export interface MadWindow {
  /** 完整工程 IR（关键帧原样全铺、零裁剪）。 */
  ir: IRProject;
  /** 子合成命名唯一化后缀源（uid，与 seq 合成唯一名）。 */
  uid: string;
  /** 窗口序号（0-based）。 */
  seq: number;
  /** 源工程时间线上的窗口起点（秒）。 */
  t0: number;
  /** 源工程时间线上的窗口终点（秒）。 */
  t1: number;
  /** 母时间线落点（秒）。 */
  dropAt: number;
  /** 母时间线上该窗口展示时长（秒；beat 量化只裁短不拉长，缺省=t1−t0）。 */
  outLen?: number;
  /** 素材注入：本窗口各 slot layerId → footage（同一素材、各 srcOffset 错开）。 */
  footage?: FootageMap;
}

/** beat marker：时刻（母时间线秒）+ 是否 downbeat。 */
export interface BeatMarker {
  t: number;
  downbeat: boolean;
}

export interface MadJsxOpts {
  /** 母合成画布。 */
  master: { w: number; h: number; fps: number; name?: string };
  /** 技法窗口序列。 */
  windows: MadWindow[];
  /** 产物 header（默认产品定稿口径由调用侧注入）。 */
  header?: string;
  /** BGM：入母合成音轨 + beat marker。 */
  bgm?: { path: string; markers?: BeatMarker[] };
}

/**
 * MAD 母合成拼接：footage 一次导入（跨子合成共享）→ 每窗口建子合成（完整 IR 全铺 + 素材注入 + 烘焙）
 * → 母合成三属性裁窗串接 + BGM 入轨 + beat marker。产单一 .jsx。
 */
export function madJsx(opts: MadJsxOpts): { jsx: string; warnings: string[] } {
  const { master, windows } = opts;
  const mw = Math.max(4, Math.round(master.w));
  const mh = Math.max(4, Math.round(master.h));
  const mfps = Math.min(99, Math.max(1, master.fps || 30));
  const masterName = esc(master.name ?? "MAD Master");

  // 全局 footage 路径（跨窗口去重、母合成级仅导入一次）
  const allPaths: string[] = [];
  for (const win of windows) {
    for (const f of Object.values(win.footage ?? {})) allPaths.push(f.path);
  }

  const ctx = newCtx({ bakeOps: true });
  const L = ctx.lines;
  L.push(opts.header ?? HEADER);
  L.push(``);
  L.push(`app.beginUndoGroup("MAD Rebuild ${masterName}");`);
  L.push(`(function () {`);

  // footage 导入（母合成级共享）
  emitFootageImports(ctx, allPaths);
  const globalFootageVars = ctx.footageVars;

  // 母合成末窗切点 → 母合成时长
  let totalDur = 0.1;
  for (const win of windows) {
    const len = win.outLen ?? win.t1 - win.t0;
    totalDur = Math.max(totalDur, win.dropAt + Math.max(0.01, len));
  }
  L.push(``);
  L.push(`  var master = app.project.items.addComp("${masterName}", ${mw}, ${mh}, 1, ${r3(totalDur)}, ${r3(mfps)});`);
  L.push(`  try { master.bgColor = [0.04,0.04,0.07]; } catch (e) {}`);

  // 逐窗口：建子合成 + 发射层体
  const subVars: { subVar: string; win: MadWindow }[] = [];
  windows.forEach((win, wi) => {
    const c = win.ir.canvas;
    const sw = Math.max(4, Math.round(num(c.w, 1920)));
    const sh = Math.max(4, Math.round(num(c.h, 1080)));
    const sfps = Math.min(99, Math.max(1, num(c.fps, 30)));
    const sdur = Math.max(0.1, num(c.duration, 3));
    const subName = esc(`${win.uid}-${win.seq}`); // 唯一命名（uid+序号）
    const subVar = `sub${wi}`;
    L.push(``);
    L.push(`  // ==== 子合成 #${wi} 技法 ${subName}（源窗 t0=${r3(win.t0)} t1=${r3(win.t1)}）====`);
    L.push(`  var ${subVar} = app.project.items.addComp("${subName}", ${sw}, ${sh}, 1, ${r3(sdur)}, ${r3(sfps)});`);

    // 该窗口 footage 映射（layerId → 全局 footage 变量已注册）
    const winFootage: FootageMap = { ...(win.footage ?? {}) };
    const subCtx: Ctx = {
      lines: L,
      warnings: ctx.warnings,
      layerSeq: 0,
      compVar: subVar,
      footage: winFootage,
      bakeOps: true,
      footageVars: globalFootageVars,
    };
    for (const ly of win.ir.layers ?? []) emitLayer(subCtx, win.ir, ly, null);
    subVars.push({ subVar, win });
  });

  // 母合成三属性裁窗串接
  L.push(``);
  L.push(`  // ==== 母合成裁窗串接（startTime=落点−t0，inPoint=落点，outPoint=落点+窗长）====`);
  subVars.forEach(({ subVar, win }, wi) => {
    const len = Math.max(0.01, win.outLen ?? win.t1 - win.t0);
    const lv = `mL${wi}`;
    L.push(`  var ${lv} = master.layers.add(${subVar});`);
    L.push(`  ${lv}.startTime = ${r3(win.dropAt - win.t0)};`);
    L.push(`  ${lv}.inPoint = ${r3(win.dropAt)};`);
    L.push(`  ${lv}.outPoint = ${r3(win.dropAt + len)};`);
    // 跨尺寸/fps cover 缩放（子合成尺寸 ≠ 母合成 → 充满母画布无黑边）
    L.push(`  try {`);
    L.push(`    var _cov = Math.max(${mw} / ${subVar}.width, ${mh} / ${subVar}.height) * 100;`);
    L.push(`    ${lv}.property("ADBE Transform Group").property("ADBE Scale").setValue([_cov, _cov]);`);
    L.push(`    ${lv}.property("ADBE Transform Group").property("ADBE Position").setValue([${mw / 2}, ${mh / 2}]);`);
    L.push(`  } catch (e) {}`);
  });

  // BGM 入母合成音轨 + beat marker
  if (opts.bgm) {
    L.push(``);
    L.push(`  // ==== BGM 入轨（importFile 包 try/catch，AE 侧文件缺失/损坏脚本继续跑完仅缺音轨）====`);
    L.push(`  try {`);
    L.push(`    var bgm = app.project.importFile(new ImportOptions(new File("${esc(fwdSlash(opts.bgm.path))}")));`);
    L.push(`    var bgmL = master.layers.add(bgm);`);
    L.push(`    bgmL.startTime = 0;`);
    L.push(`  } catch (e) {}`);
    const markers = opts.bgm.markers ?? [];
    if (markers.length) {
      L.push(`  // beat marker（downbeat 带标签区分）`);
      L.push(`  try {`);
      L.push(`    var mk = master.property("ADBE Marker");`);
      for (const m of markers) {
        const label = m.downbeat ? "downbeat" : "beat";
        L.push(`    mk.setValueAtTime(${r3(m.t)}, new MarkerValue("${label}"));`);
      }
      L.push(`  } catch (e) {}`);
    }
  }

  L.push(``);
  L.push(`  master.openInViewer();`);
  L.push(`})();`);
  L.push(`app.endUndoGroup();`);
  return { jsx: L.join("\n") + "\n", warnings: ctx.warnings };
}
